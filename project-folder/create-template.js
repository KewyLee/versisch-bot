const PDFDocument = require('pdfkit');
const fs = require('fs');

// Создаем новый PDF документ
const doc = new PDFDocument();

// Направляем вывод в файл
doc.pipe(fs.createWriteStream('template.pdf'));

// Добавляем содержимое
doc.fontSize(16).text('Шаблон формы для заполнения', 100, 100);
doc.fontSize(12).text('Имя, Фамилия: ___________________', 100, 150);
doc.text('Фамилия при рождении: ___________________', 100, 180);
doc.text('Дата рождения: ___________________', 100, 210);
doc.text('Родной город: ___________________', 100, 240);
doc.text('Адрес: ___________________', 100, 270);
doc.text('Email: ___________________', 100, 300);
doc.text('Телефон: ___________________', 100, 330);
doc.text('Подпись:', 100, 380);

// Создаем поле для подписи
doc.rect(100, 400, 200, 80).stroke();

// Завершаем документ
doc.end();

console.log('Шаблон PDF успешно создан!'); 